//String Operators: + (concatenation), += (concatenation assignment) 


let str1 = "Hello, ";
let str2 = "world!";

let result = str1 + str2; // The concatenation operator (+) is used to combine the two strings str1 and str2, resulting in "Hello, world!".

console.log(result); // Output: "Hello, world!"

