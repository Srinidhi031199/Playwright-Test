//Assignment operator'

let x = 5; // The assignment operator (=) is used to assign the value 5 to the variable x.

//x = x + 10; // This is a common way to add a value to a variable and assign the result back to that variable. It can be simplified using the addition assignment operator (+=).

x += 10; // The addition assignment operator (+=) is a shorthand for adding a value to a variable and assigning the result back to that variable.

x -= 5; // The subtraction assignment operator (-=) is a shorthand for subtracting a value from a variable and assigning the result back to that variable.

console.log(x); // Output: 10 (5 + 10 - 5 = 10)

x *= 2; // The multiplication assignment operator (*=) is a shorthand for multiplying a variable by a value and assigning the result back to that variable.

console.log(x); // Output: 20 (10 * 2 = 20)

x /= 4; // The division assignment operator (/=) is a shorthand for dividing a variable by a value and assigning the result back to that variable.

console.log(x); // Output: 5 (20 / 4 = 5)

x %= 3; // The modulus assignment operator (%=) is a shorthand for finding the remainder when a variable is divided by a value and assigning the result back to that variable.

console.log(x); // Output: 2 (5 % 3 = 2)