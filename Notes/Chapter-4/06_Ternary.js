// Ternary Operator : condition ? expressionIfTrue : expressionIfFalse

let age = 18;

let result = age >= 18 ? "Yes, you can vote." : "No, you cannot vote."; // The ternary operator checks if the condition (age >= 18) is true. If it is true, it returns "Yes, you can vote." Otherwise, it returns "No, you cannot vote."

console.log(result); // Output: "Yes, you can vote."

console.log("------------------------------");

// it is a short hand for if else statement
// : is a division between true and false part of the statement