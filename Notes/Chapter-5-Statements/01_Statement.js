// Statements in JavaScript: 

// A statement is a piece of code that performs an action. It can be a declaration, an expression, a control flow statement, or a function call. Statements are executed sequentially in the order they appear in the code.

let x = 10; // Variable declaration statement: This statement declares a variable named x and assigns it the value of 10.

console.log(x); // Function call statement: This statement calls the console.log function to output the value of x to the console.

// if else statement:

let age = 18;

if (age >= 18) {
    console.log("You are an adult."); // Output: "You are an adult." (Since age is 18, the condition is true and this block of code is executed)
} else {
    console.log("You are a minor."); // This block of code will not be executed since the condition is true
}

