//increment / decrement

//pre-increment: ++a
//post-increment: a++

//pre-decrement: --a
//post-decrement: a--

console.log("------------------------------");
//pre-increment: ++a
let a = 10;
let b = ++a; // (++a -> 10+1) The pre-increment operator (++a) increases the value of a by 1 before it is used in the expression.

console.log(a); // Output: 11
console.log(b); // Output: 11

console.log("------------------------------");
//post-increment: a++
let c = 10;
let d = c++; // (c++ -> 10) The post-increment operator (c++) increases the value of c by 1 after it is used in the expression.

console.log(c); // Output: 11
console.log(d); // Output: 10

console.log("------------------------------");

